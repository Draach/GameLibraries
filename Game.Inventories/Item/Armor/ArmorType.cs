namespace Game.Inventories
{
    public enum ArmorType
    {
        /// <summary>
        /// Lightweight and made of fabric or leather, provides minimal protection against physical attacks but often boosts magical abilities.
        /// </summary>
        Cloth,
        /// <summary>
        /// Made of tough leather, provides moderate protection against physical attacks and allows for greater mobility and flexibility.
        /// </summary>
        Leather,
        /// <summary>
        /// Made of interlocking metal rings, provides good protection against physical attacks but can be heavy and restrictive.
        /// </summary>
        ChainMail,
        /// <summary>
        /// Made of solid metal plates, provides the highest level of protection against physical attacks but is also the heaviest and most cumbersome.
        /// </summary>
        Plate,
        /// <summary>
        /// Defensive items that can be used in conjunction with armor to provide additional protection against physical attacks.
        /// </summary>
        Shield,
        /// <summary>
        /// Long, flowing garments made of lightweight fabric, often used by spellcasters to boost their magical abilities while sacrificing physical protection.
        /// </summary>
        Robe,
        /// <summary>
        /// Protective headgear that provides additional defense against head injuries and other critical hits.
        /// </summary>
        Helmet,
    }
}